                    <div class="parmis col-md-7">
                        <h4>L’ORIGINE DE LA CAMPAGNE</h4>
                        <p>
                            Initiée par Chouna Lomponda, cette campagne fait suite à une parution d'actualité,
                            parue fin 2017 qui traitait de la question de la carence des diplômes et de la pauvreté à Bruxelles.<br><br>
                            En guise d’illustration ce GRAND QUOTIDIEN a utilisé UNE PHOTO sur laquelle figuraient
                            de jeunes adolescents « NOIRS » ET « ARABES ».<br>

                            Il s’agit d’un fait parmi tant d’autres qui témoignent de
                            l’utilisation d’images réductrices auxquelles certains médias n’échappent pas toujours aujourd’hui.
                            Ici, en l’occurrence,  l’illustration de l’article laisse à penser qu’on CONFÈRE une connotation raciale.<br><br>
                            Cet article a ainsi fait émerger l’idée du lancement d’une campagne pour lutter contre certains clichés dont font parfois l’objet les personnes issues
                            des minorités.<br>
                            Cette campagne consiste en une réponse élégante, constructive, humoristique et surtout pédagogique à l’adresse des jeunes, des médias, du grand
                            public et des décideurs. <br><br><br>
                        </p>
                    </div>

                    <div class="parmis col-md-5">
                        <h4>LES OBJECTIFS DE LA CAMPAGNE</h4>
                        <p>
                            <ul>
                              <li>Sensibiliser les médias aux images qu’ils véhiculent
                                auprès du grand public pour ce qui concerne les sujets se
                                rapportant aux minorités. <br><br> </li>
                              <li>
                                Sensibiliser et présenter à la jeunesse de nouveaux «rôles- modèles», en vue de lutter contre toute forme de
                                discrimination.<br><br>
                              </li>
                              <li>Susciter auprès des jeunes générations un sentiment de reconnaissance et
                                des appartenances communautaires. <br> <br> </li>
                              <li>Lutter contre les stéréotypes. <br><br> </li>
                            </ul>
                        </p>
                    </div>

